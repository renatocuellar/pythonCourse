"""
Declara dos conjuntos: A = {1, 2, 3, 4} y B = {3, 4, 5, 6}. Calcula y muestra su unión, intersección, y diferencia.
"""

A = {1, 2, 3, 4}
B = {3, 4, 5, 6}

my_union = A.union(B)
my_intersection = A.intersection(B)
my_difference = A.difference(B)

print(my_union)
print(my_intersection)
print(my_difference)