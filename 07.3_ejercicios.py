"""
Crea una tupla con cinco números. Calcula el promedio de los valores en la tupla y guárdalo en una variable.
"""

# Crear una tupla con cinco números
my_tuple = (5, 7, 9, 11, 13)

# Calcular la suma de los elementos de la tupla
suma = sum(my_tuple)

# Calcular el promedio
promedio = suma / len(my_tuple)

# Mostrar el promedio
print(promedio)